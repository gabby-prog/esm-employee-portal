function serviceEndDate(emp){
  if(emp && emp.status === 'Inactive' && stopServiceReason(emp.inactiveReason) && emp.inactiveDate) return parseDate(emp.inactiveDate);
  return endOfToday();
}
function creditedServiceMs(emp){
  const start=parseDate(emp.hireDate);
  const end=serviceEndDate(emp);
  return Math.max(0, (end-start) - servicePauseMs(emp));
}
function yearsOfService(input){
  if(typeof input === 'object') return Math.floor(creditedServiceMs(input)/(365.25*24*60*60*1000));
  const start=parseDate(input), today=endOfToday();
  let years=today.getFullYear()-start.getFullYear();
  const anniv=new Date(today.getFullYear(), start.getMonth(), start.getDate());
  if(today < anniv) years--;
  return Math.max(0, years);
}
function yearsDecimal(input){
  if(typeof input === 'object') return Math.max(0, creditedServiceMs(input)/(365.25*24*60*60*1000));
  const start=parseDate(input), today=endOfToday();
  return Math.max(0, (today-start)/(365.25*24*60*60*1000));
}
function serviceDateForMilestone(emp, years){
  const base = addYears(parseDate(emp.hireDate), years);
  return new Date(base.getTime() + servicePauseMs(emp));
}
function vacationWeeks(years){ if(years < 1) return 0; if(years <= 5) return 1; if(years <= 10) return 2; if(years <= 20) return 3; return 4; }
function nextAnniversary(input){
  if(typeof input === 'object'){
    const emp=input, today=endOfToday();
    const credited=yearsDecimal(emp);
    const nextYear=Math.floor(credited)+1;
    return serviceDateForMilestone(emp, nextYear);
  }
  const start=parseDate(input), today=endOfToday();
  let next=new Date(today.getFullYear(), start.getMonth(), start.getDate());
  if(next < today) next.setFullYear(next.getFullYear()+1);
  return next;
}
function nextVacationMilestone(input){
  const today=endOfToday();
  const milestones=[1,6,11,21];
  if(typeof input === 'object'){
    const emp=input;
    for(const y of milestones){
      const d=serviceDateForMilestone(emp,y);
      if(d >= today) return {years:y, weeks:vacationWeeks(y), date:d};
    }
    return null;
  }
  const start=parseDate(input);
  for(const y of milestones){
    const d=addYears(start,y);
    if(d >= today) return {years:y, weeks:vacationWeeks(y), date:d};
  }
  return null;
}
function getPtoEntries(emp){
  const adminEntries = Array.isArray(emp.ptoUsage) ? emp.ptoUsage : [];
  const approvedSheetEntries = Array.isArray(emp.livePtoUsage) ? emp.livePtoUsage : [];
  return [...adminEntries, ...approvedSheetEntries];
}
function normalizePtoType(type){
  const t = String(type || '').toLowerCase();
  if(t.includes('vacation')) return 'Vacation';
  if(t.includes('sick')) return 'Sick';
  if(t.includes('personal')) return 'Personal';
  return String(type || '').trim();
}
function parsePtoAmount(raw, type){
  const t = normalizePtoType(type);
  // ESM rule: sick and personal requests count as one day per approved submission.
  // The hours field on the Google Form is ignored for these PTO types.
  if(t === 'Sick' || t === 'Personal') return 1;
  if(raw === null || raw === undefined || raw === '') return 0;
  if(typeof raw === 'number') return normalizePtoAmountUnits(raw, type);
  const text = String(raw).toLowerCase().trim();
  if(!text) return 0;
  const nums = [...text.matchAll(/\d+(?:\.\d+)?/g)].map(m => Number(m[0]));
  if(!nums.length) return 0;
  // Vacation can be entered directly in week/quarter increments, such as 0.25, 0.5, 0.75, or 1.
  if(t === 'Vacation' && nums.length === 1 && nums[0] <= 4 && !text.includes('hour') && !text.includes('hr') && !text.includes('day')) return nums[0];
  let hours = nums[0];
  if(text.includes('day')) hours = nums[0] * PTO_HOURS_PER_DAY;
  if(text.includes('hr') || text.includes('hour')) hours = nums[0];
  if(text.includes('min') && nums.length > 1) hours = nums[0] + (nums[1] / 60);
  return normalizePtoAmountUnits(hours, type);
}
function normalizePtoAmountUnits(hours, type){
  const t = normalizePtoType(type);
  const n = Number(hours || 0);
  if(t === 'Vacation') return +(n / VACATION_HOURS_PER_WEEK).toFixed(2);
  if(t === 'Sick' || t === 'Personal') return 1;
  return n;
}
function displayPtoAmount(entry){
  const n = Number(entry.amount || 0);
  if(entry.type === 'Vacation') return `${n} week(s)`;
  if(entry.type === 'Sick' || entry.type === 'Personal') return `${n} day(s)`;
  return entry.amountDisplay || String(n);
}
function currentBenefitYear(){ return new Date().getFullYear(); }
function isCurrentBenefitYearEntry(entry){
  const iso = entry.dateUsed || entry.dateSubmitted || '';
  if(!iso) return true;
  const d = new Date(iso);
  if(isNaN(d.getTime())) return true;
  return d.getFullYear() === currentBenefitYear();
}
function sumPtoUsage(emp, type){
  const entries = getPtoEntries(emp).filter(isCurrentBenefitYearEntry);
  return entries.filter(x => x.type === type).reduce((sum,x)=>sum + Number(x.amount || 0), 0);
}
function usedOverrideValue(emp, key){
  const v = emp?.pto?.[key];
  return (v === null || v === undefined || v === '') ? null : Number(v);
}
function autoBenefits(emp){
  const yrs = yearsOfService(emp);
  const decimal = yearsDecimal(emp);
  const p = emp.pto || {};
  const overrides = emp.overrides || {};
  const benefitEligible = yrs >= 1;
  const vacationEarned = p.vacationEarnedOverride ?? vacationWeeks(yrs);
  const sickEarned = p.sickEarnedOverride ?? (benefitEligible ? 3 : 0);
  const personalEarned = p.personalEarnedOverride ?? (benefitEligible ? 3 : 0);
  const vacationUsed = usedOverrideValue(emp, 'vacationUsedOverride') ?? sumPtoUsage(emp, 'Vacation') ?? Number(p.vacationUsed || 0);
  const sickUsed = usedOverrideValue(emp, 'sickUsedOverride') ?? sumPtoUsage(emp, 'Sick') ?? Number(p.sickUsed || 0);
  const personalUsed = usedOverrideValue(emp, 'personalUsedOverride') ?? sumPtoUsage(emp, 'Personal') ?? Number(p.personalUsed || 0);
  return {
    years: yrs,
    yearsDecimal: decimal,
    ptoEligible: benefitEligible,
    vacationEarned, vacationUsed, vacationRemaining: Math.max(0, vacationEarned - vacationUsed),
    sickEarned, sickUsed, sickRemaining: Math.max(0, sickEarned - sickUsed),
    personalEarned, personalUsed, personalRemaining: Math.max(0, personalEarned - personalUsed),
    hcoEligible: !!overrides.hco,
    erspEligible: !!overrides.ersp,
    tuitionEligible: !!overrides.tuition || benefitEligible,
    hasOverride: !!(overrides.hco || overrides.ersp || overrides.tuition || p.vacationEarnedOverride || p.sickEarnedOverride || p.personalEarnedOverride),
    nextAnniversary: nextAnniversary(emp),
    nextVacationMilestone: nextVacationMilestone(emp)
  };
}
function normalizeName(s){ return String(s||'').toLowerCase().replace(/[^a-z ]/g,'').replace(/\s+/g,' ').trim(); }
function normalizePin(s){ return String(s||'').replace(/\D/g,'').padStart(4,'0').slice(-4); }
