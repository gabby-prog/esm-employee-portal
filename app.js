async function loadLivePtoUsage(){
  if(!GOOGLE_SHEET_API_URL) return;
  try {
    const response = await fetch(GOOGLE_SHEET_API_URL, {cache:'no-store'});
    if(!response.ok) throw new Error(`Google Sheet request failed: ${response.status}`);
    const data = await response.json();
    const rows = Array.isArray(data.ptoUsage) ? data.ptoUsage : [];
    applyLivePtoUsage(rows);
    livePtoUsageLoaded = true;
    livePtoUsageError = '';
  } catch(err) {
    console.warn('Could not load live PTO usage from Google Sheets.', err);
    livePtoUsageLoaded = false;
    livePtoUsageError = err.message || 'Unable to load Google Sheet data.';
  }
}
function googleValue(row, names){
  for(const name of names){
    if(Object.prototype.hasOwnProperty.call(row, name)) return row[name];
  }
  return '';
}
function ptoRequestStatus(row){
  return String(googleValue(row, ['Column 1','Status','Paid','Processed','Approval Status','status']) || '').toLowerCase().trim();
}
function isApprovedPtoStatus(status){
  return status.includes('paid') || status.includes('processed') || status.includes('approved');
}
function sheetPtoRecord(row, index){
  const name = String(googleValue(row, ['Name','name']) || '').trim();
  const rawType = googleValue(row, ['Type','type','PTO Type']);
  const type = normalizePtoType(rawType);
  if(!name || !['Vacation','Sick','Personal'].includes(type)) return null;
  const rawAmount = googleValue(row, ['Amount ','Amount','amount']);
  const amount = parsePtoAmount(rawAmount, type);
  const dateUsedRaw = googleValue(row, ['Date Used','Date','dateUsed','date']);
  const dateSubmittedRaw = googleValue(row, ['Date Submitted','Timestamp','Submitted','dateSubmitted']);
  return {
    id: `GSHEET${index}`,
    name,
    dateUsed: googleDateToIso(dateUsedRaw),
    type,
    amount,
    amountDisplay: String(rawAmount || ''),
    dateSubmitted: googleDateToIso(dateSubmittedRaw),
    desiredPaycheck: String(googleValue(row, ['Desired Pay Check ','Desired Pay Check','Paycheck','Desired Paycheck']) || ''),
    status: ptoRequestStatus(row),
    notes: 'Approved Google Sheet PTO record',
    source: 'Google Sheet'
  };
}
function applyLivePtoUsage(rows){
  const grouped = {};
  pendingPtoRequests = [];
  rows.forEach((row, index) => {
    const record = sheetPtoRecord(row, index);
    if(!record) return;
    const decision = ptoRequestDecisions[ptoDecisionKey(record)];
    if(decision?.status === 'approved' || isApprovedPtoStatus(record.status)){
      if(!record.amount) record.amount = record.type === 'Vacation' ? 1 : 1;
      record.notes = decision?.note || record.notes || 'Approved PTO record';
      const key = normalizeName(record.name);
      if(!grouped[key]) grouped[key] = [];
      grouped[key].push(record);
    } else if(decision?.status === 'denied') {
      record.notes = decision.note || 'Denied';
    } else {
      pendingPtoRequests.push(record);
    }
  });
  employees.forEach(emp => {
    emp.livePtoUsage = grouped[normalizeName(emp.name)] || [];
  });
}
function googleDateToIso(value){
  if(!value) return '';
  if(value instanceof Date) return value.toISOString().slice(0,10);
  const parsed = new Date(value);
  if(!isNaN(parsed.getTime())) return parsed.toISOString().slice(0,10);
  return String(value).slice(0,10);
}

