function vacationTierProgress(emp){
  const b=autoBenefits(emp);
  const m=b.nextVacationMilestone;
  if(!m) return '<div class="progress-label">Highest vacation tier reached.</div><div class="progress-track"><div class="progress-fill" style="width:100%"></div></div>';
  const currentYears=b.yearsDecimal;
  const previous=[1,6,11,21].filter(x=>x<=currentYears).pop() || 0;
  const span=m.years-previous;
  const pct=Math.max(0,Math.min(100,((currentYears-previous)/span)*100));
  return `<div class="progress-label">${(m.years-currentYears).toFixed(1)} years until next vacation increase</div><div class="progress-track"><div class="progress-fill" style="width:${pct}%"></div></div>`;
}
function employmentHistoryRows(emp){
  const blocks = Array.isArray(emp.employmentHistory) && emp.employmentHistory.length ? emp.employmentHistory : [{startDate: emp.hireDate, inactiveDate: emp.inactiveDate, reactivationDate: emp.reactivationDate, reason: emp.inactiveReason, active: emp.status === 'Active'}];
  return blocks.map(block=>{
    const start=block.startDate || emp.hireDate;
    const end=block.inactiveDate || (block.active ? '' : emp.inactiveDate);
    const react=block.reactivationDate || emp.reactivationDate || '';
    const lengthEnd=end ? parseDate(end) : endOfToday();
    const yrs=Math.max(0,(lengthEnd-parseDate(start))/(365.25*24*60*60*1000));
    return `<tr><td>${fmtDate(start)}</td><td>${end?fmtDate(end):'Active'}</td><td>${react?fmtDate(react):'—'}</td><td>${escapeHtml(block.reason||emp.inactiveReason||'—')}</td><td>${yrs.toFixed(1)} years</td></tr>`;
  }).join('');
}
