function exportPortalBackup(){
  const payload = {
    app: 'ESM Employee Portal',
    backupVersion: '1.0',
    exportedAt: new Date().toISOString(),
    storageKey: STORAGE_KEY,
    employeeCount: employees.length,
    employees: employees
  };
  const dateStamp = new Date().toISOString().slice(0,10);
  const blob = new Blob([JSON.stringify(payload, null, 2)], {type:'application/json'});
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `esm-portal-data-backup-${dateStamp}.json`;
  a.click();
  URL.revokeObjectURL(url);
}
function importPortalBackup(ev){
  const file = ev.target.files && ev.target.files[0];
  if(!file) return;
  const reader = new FileReader();
  reader.onload = () => {
    try {
      const parsed = JSON.parse(reader.result);
      const records = Array.isArray(parsed) ? parsed : parsed.employees;
      if(!Array.isArray(records) || records.length === 0) throw new Error('No employee records found.');
      const currentCount = employees.length;
      const ok = confirm(`Import ${records.length} employee records from this backup? This will replace the portal data currently saved in this browser (${currentCount} records).`);
      if(!ok) return;
      const freshSeed = JSON.parse(JSON.stringify(SEED_EMPLOYEES));
      const pinMap = Object.fromEntries(freshSeed.map(e => [e.id, e.pin || '']));
      const namePinMap = Object.fromEntries(freshSeed.map(e => [normalizeName(e.name), e.pin || '']));
      employees = records.map(e => normalizeEmployeeRecord(e, pinMap, namePinMap));
      saveAll();
      renderAllAdmin();
      renderEmployeeSearch();
      alert(`Portal data imported successfully. ${employees.length} employee records are now saved in this browser.`);
    } catch(err) {
      console.error(err);
      alert('This file could not be imported. Please choose a valid ESM portal backup JSON file.');
    } finally {
      ev.target.value = '';
    }
  };
  reader.readAsText(file);
}

